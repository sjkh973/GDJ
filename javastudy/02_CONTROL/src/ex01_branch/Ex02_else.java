package ex01_branch;

public class Ex02_else {

	public static void main(String[] args) {
		
		/*
		 * if-else문
		 * 조건을 만족하는 경우와 그렇지 않은 경우 모두 처리한다.
		 * if(조건){
		 * 		실행문
		 * 
		 * } else{
		 *		실행문 
		 * }
		 */

		int score = 100;
		
		if(score >= 60) {
			System.out.println("합격");
		} else {
			System.out.println("불합격");
		}
	}

}
